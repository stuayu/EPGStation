    /**
     * チャンネル情報からニコニコ実況の実況チャンネル ID (jk1 など) を解決する
     * @param channel: ChannelInfo
     * @return string | null 解決できなかった場合は null
     */
    export const findJikkyoChannelId = (channel: ChannelInfo): string | null => {
        if (channel.networkId >= GR_NETWORK_ID_MIN && channel.networkId <= GR_NETWORK_ID_MAX) {
            // 地上波 (GR / NW1〜NW40)
            // 対照表にはサブチャンネルが定義されていないため、
            // serviceId - 1 / - 2 も探索する (例: 1025 [NHK総合2・東京] -> 1024 [NHK総合1・東京])
            for (const serviceId of [channel.serviceId, channel.serviceId - 1, channel.serviceId - 2]) {
                if (typeof GR_SERVICE_ID_TABLE[serviceId] !== 'undefined') {
                    return 'jk' + GR_SERVICE_ID_TABLE[serviceId].toString(10);
                }
            }

            return null;
        }

        const key = `${channel.networkId}:${channel.serviceId}`;

        return typeof BS_CS_TABLE[key] === 'undefined' ? null : 'jk' + BS_CS_TABLE[key].toString(10);
    };
